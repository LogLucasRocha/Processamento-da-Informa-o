public class ContagemRegressiva {

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        
        while (n > 0){
            System.out.println(n);
            n -= 1;
        }
        
        System.out.println("Boom!!!");
  
    }
    
    
}
